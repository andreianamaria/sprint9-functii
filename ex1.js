// Scrieți o funcție care returnează pătratul unui număr
// Funcția findSquare(num) returnează pătratul numărului primit ca date de intrare 
// (este transmis ca argument în apelul funcției)

const findSquare = (n) => n * n;

const findSquareTwo = (n) => n ** 2;

console.log(findSquare(6));
console.log(findSquare(10));
console.log(findSquare(0));
console.log(findSquare(25));
console.log(findSquare(-12));


console.log(findSquareTwo(6));
console.log(findSquareTwo(10));
console.log(findSquareTwo(0));
console.log(findSquareTwo(25));
console.log(findSquareTwo(-12));